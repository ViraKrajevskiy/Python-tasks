
# 1. git init
#    Создает новый репозиторий Git в текущей директории.
#
# 2. git clone <url>
#    Клонирует существующий репозиторий с указанного URL на локальный компьютер.
#
# 3. git status
#    Показывает статус текущего репозитория: измененные файлы, файлы, готовые для коммита, и файлы без отслеживания.
#
# 4. git add <file>
#    Добавляет файл или файлы в индекс (staging area) для подготовки к коммиту.
#
# 5. git commit -m "<message>"
#    Фиксирует изменения в репозитории с описанием (комментариями).
#
# 6. git log
#    Показывает историю коммитов в текущей ветке.
#
# 7. git branch
#    Показывает список веток в репозитории. Добавьте имя для создания новой ветки.
#
# 8. git branch -d <branch>
#    Удаляет указанную ветку.
#
# 9. git checkout <branch>
#    Переключается на указанную ветку.
#
# 10. git merge <branch>
#     Сливает указанную ветку с текущей.
#
# 11. git pull
#     Загружает изменения из удаленного репозитория и сливает их с текущей веткой.
#
# 12. git push
#     Отправляет изменения из локального репозитория в удаленный.
#
# 13. git remote add <name> <url>
#     Добавляет удаленный репозиторий с указанным URL.
#
# 14. git remote -v
#     Показывает список подключенных удаленных репозиториев.
#
# 15. git fetch
#     Загружает изменения из удаленного репозитория, но не сливает их.
#
# 16. git diff
#     Показывает различия между текущими изменениями и последним коммитом.
#
# 17. git reset <file>
#     Убирает файл из индекса (staging area), но сохраняет изменения в рабочей директории.
#
# 18. git reset --hard
#     Отменяет все изменения и возвращает репозиторий к последнему коммиту.
#
# 19. git stash
#     Сохраняет текущие изменения без коммита и очищает рабочую директорию.
#
# 20. git stash pop
#     Восстанавливает изменения, сохраненные в stash, и удаляет их из хранилища.
#
# 21. git rebase <branch>
#     Переносит изменения из одной ветки в другую, изменяя историю коммитов.
#
# 22. git tag <tagname>
#     Создает тег для фиксации в истории, например, для релиза.
#
# 23. git show <tag/branch>
#     Показывает информацию о теге или ветке.
#
# 24. git cherry-pick <commit>
#     Применяет изменения из указанного коммита в текущую ветку.
#
# 25. git revert <commit>
#     Создает новый коммит, который отменяет изменения указанного коммита.
#
